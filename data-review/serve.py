def atomic_write_text(path: Path, content: str) -> None:
    """Write `content` to `path` atomically: temp file + rename."""
    fd, tmp = tempfile.mkstemp(dir=str(path.parent), prefix=".tmp.", suffix=".json")
    try:
        with os.fdopen(fd, "w") as f:
            f.write(content)
        os.replace(tmp, path)
    except Exception:
        try:
            os.unlink(tmp)
        except FileNotFoundError:
            pass
        raise


def append_log(record: dict) -> None:
    """Append one JSONL line to the comments log (no-fsync; OS-buffered ok)."""
    with open(LOG_PATH, "a") as f:
        f.write(json.dumps(record, separators=(",", ":")) + "\n")


import re as _re
_TIMESTAMP_LINE_RE = _re.compile(r"_Auto-generated by `[^`]+` on \*\*[^*]+\*\*\._")


def _has_substantive_diff(rel_path: str, repo: Path) -> bool:
    """Return True iff the working-tree file differs from HEAD in any line
    OTHER than the auto-generated timestamp banner. Without this, every
    rebuild would create a "useless" diff (timestamp moved by 1 sec) and
    sync would always commit + push even when nothing else changed."""
    abs_path = repo / rel_path
    if not abs_path.exists():
        return False
    try:
        head = subprocess.run(
            ["git", "show", f"HEAD:{rel_path}"],
            cwd=str(repo), capture_output=True, text=True,
        )
        if head.returncode != 0:
            # untracked / not in HEAD → genuinely new
            return True
    except Exception:
        return True
    cur = abs_path.read_text()
    return _TIMESTAMP_LINE_RE.sub("", cur) != _TIMESTAMP_LINE_RE.sub("", head.stdout)


def commit_and_push_audit_logs() -> dict:
    """Regenerate the audit log, then `git add` + `git commit` + `git push`
    the markdown files. Used by BOTH the manual `/sync` endpoint and the
    Ctrl+C shutdown handler.

    Returns a dict describing what happened — caller renders to user. Keys:
      ok           — overall success (commit landed, push attempted)
      committed    — bool, did we actually create a commit?
      pushed       — bool, did `git push` succeed?
      message      — human-readable status line
      sha          — committed sha if any
      error        — populated on any failure
    """
    repo = ROOT.parent
    files = [
        # vetting.jsonl is the SOURCE OF TRUTH for review decisions -- commit
        # it first so approvals survive even if the worktree is wiped. The
        # docs/*.md are the regenerated human-readable views.
        "audit_dashboard/vetting.jsonl",
        "docs/GMD_AUDIT_LOG.md",
        "docs/SOURCE_ISSUES.md",
        "docs/AUDIT_CONSOLIDATED.md",
    ]
    # 1) regenerate so the on-disk MDs reflect the very latest comments.json
    try:
        maybe_rebuild_audit_log()
    except Exception as e:
        return {"ok": False, "committed": False, "pushed": False,
                "error": f"rebuild failed: {e}",
                "message": "audit-log rebuild failed — nothing pushed"}
    branch = subprocess.run(["git", "rev-parse", "--abbrev-ref", "HEAD"],
                            cwd=str(repo), capture_output=True, text=True).stdout.strip()
    head_before = subprocess.run(["git", "rev-parse", "HEAD"], cwd=str(repo),
                                 capture_output=True, text=True).stdout.strip()

    # 2) detect whether any of the target files has a SUBSTANTIVE diff vs HEAD
    # (ignoring the auto-generated timestamp line, otherwise every rebuild
    # looks like a real change).
    changed = []
    for rel in files:
        if (repo / rel).exists() and _has_substantive_diff(rel, repo):
            changed.append(rel)
    committed = False
    sha = head_before
    if changed:
        # 3) stage + commit (only the audit-log files; other working-tree
        # changes are deliberately left alone)
        try:
            subprocess.run(["git", "add", "--"] + changed, cwd=str(repo),
                           check=True, capture_output=True, text=True)
            ts = _dt.datetime.now().strftime("%Y-%m-%d %H:%M")
            ncom = "?"
            try:
                ncom = str(len(json.loads(COMMENTS_PATH.read_text() or "{}")))
            except Exception:
                pass
            msg = f"audit: sync GMD_AUDIT_LOG.md ({ncom} reviews, {ts})"
            subprocess.run(["git", "commit", "-m", msg], cwd=str(repo),
                           check=True, capture_output=True, text=True)
            sha = subprocess.run(["git", "rev-parse", "HEAD"], cwd=str(repo),
                                 capture_output=True, text=True).stdout.strip()
            committed = True
        except subprocess.CalledProcessError as e:
            err = (e.stderr or e.stdout or "").strip() if hasattr(e, "stderr") else str(e)
            return {"ok": False, "committed": False, "pushed": False,
                    "error": err, "message": f"commit failed: {err}"}
    else:
        # Quietly revert the timestamp-only diffs so `git status` doesn't nag.
        for rel in files:
            if (repo / rel).exists():
                subprocess.run(["git", "checkout", "HEAD", "--", rel],
                               cwd=str(repo), capture_output=True)

    # 4) integrate the remote (ALWAYS — even with a clean tree). Sync should
    # PULL new code/vetting from GitHub, not just push ours. fetch + rebase
    # replays any local commit on top of origin/branch. On conflict we abort
    # and keep our commit local rather than leaving a half-rebased tree.
    pulled = 0
    pulled_files: list[str] = []
    try:
        fetch = subprocess.run(["git", "fetch", "origin", branch],
                               cwd=str(repo), capture_output=True, text=True, timeout=60)
        if fetch.returncode == 0:
            # commits on origin we don't yet have (relative to pre-commit HEAD)
            rl = subprocess.run(
                ["git", "rev-list", "--count", f"{head_before}..origin/{branch}"],
                cwd=str(repo), capture_output=True, text=True)
            pulled = int(rl.stdout.strip() or "0") if rl.returncode == 0 else 0
            if pulled:
                df = subprocess.run(
                    ["git", "diff", "--name-only", f"{head_before}", f"origin/{branch}"],
                    cwd=str(repo), capture_output=True, text=True)
                pulled_files = [f for f in df.stdout.splitlines() if f.strip()]
            rb = subprocess.run(["git", "rebase", f"origin/{branch}"],
                                cwd=str(repo), capture_output=True, text=True, timeout=60)
            if rb.returncode != 0:
                subprocess.run(["git", "rebase", "--abort"], cwd=str(repo),
                               capture_output=True, text=True)
                err = (rb.stderr or rb.stdout or "").strip()
                tail = f"committed {sha[:10]} locally — " if committed else ""
                return {"ok": False, "committed": committed, "pushed": False,
                        "sha": sha[:10], "error": err,
                        "message": f"{tail}rebase onto origin/{branch} failed "
                                   f"(conflict); resolve manually: {err[:200]}"}
    except Exception as e:
        print(f"[warn] sync fetch/rebase skipped: {e}", file=sys.stderr)

    # 5) push our commit if we made one.
    pushed = False
    push_err = ""
    if committed:
        try:
            push = subprocess.run(["git", "push", "origin", branch], cwd=str(repo),
                                  capture_output=True, text=True, timeout=60)
            pushed = push.returncode == 0
            if not pushed:
                push_err = (push.stderr or push.stdout or "").strip()
        except Exception as e:
            push_err = str(e)

    # 6) if origin brought in code/data (anything beyond our own vetting.jsonl),
    # rebuild the dashboard data so a browser refresh shows it. flags.parquet
    # itself is left to the autoserve supervisor (its recompute is slow).
    code_updated = bool(pulled_files and any(
        f != "audit_dashboard/vetting.jsonl" for f in pulled_files))
    if code_updated:
        try:
            subprocess.run([sys.executable, "audit_dashboard/build_data.py"],
                           cwd=str(repo), capture_output=True, text=True, timeout=600)
        except Exception as e:
            print(f"[warn] data.json rebuild after pull failed: {e}", file=sys.stderr)

    # 7) compose the status line.
    parts = []
    if committed:
        parts.append(f"pushed {sha[:10]}" if pushed else f"committed {sha[:10]} locally")
    if pulled:
        refresh = " — refresh your browser" if code_updated else ""
        parts.append(f"pulled {pulled} new commit(s) from GitHub{refresh}")
    if not parts:
        parts.append("up to date — nothing to push or pull")
    ok = (pushed or not committed) and not push_err
    out = {"ok": ok, "committed": committed, "pushed": pushed,
           "pulled": pulled, "code_updated": code_updated,
           "sha": sha[:10], "message": "; ".join(parts)}
    if push_err:
        out["error"] = push_err
        out["message"] += f" — push failed: {push_err}"
    return out


def final_commit_and_push() -> None:
    """Ctrl+C shutdown wrapper around commit_and_push_audit_logs() — prints
    status to stdout instead of returning JSON."""
    r = commit_and_push_audit_logs()
    print(f"  {r.get('message','(no message)')}")
    if r.get("error") and not r.get("pushed"):
        print(f"       (commit kept locally — run `git push` from "
              f"{ROOT.parent} to retry)")


def maybe_rebuild_audit_log() -> None:
    """Run build_audit_log.py in-process to refresh docs/GMD_AUDIT_LOG.md.

    Best-effort: any error is logged but does not break the save (the actual
    persistence to comments.json + comments.log is already done by the time
    we get here)."""
    if not AUTO_REBUILD_AUDIT_LOG:
        return
    if not BUILD_AUDIT_LOG.exists():
        return
    try:
        # Run as a subprocess so a syntax error in build_audit_log.py never
        # crashes the serve.py interpreter.
        subprocess.run(
            [sys.executable, str(BUILD_AUDIT_LOG)],
            cwd=str(ROOT.parent),
            timeout=20,
            check=False,
            capture_output=True,
        )
    except Exception as e:
        print(f"[warn] audit log rebuild failed: {e}")


